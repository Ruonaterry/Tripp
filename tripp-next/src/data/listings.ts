// Helper function to check if we're in the browser
const isBrowser = typeof window !== 'undefined'

// Function to add new properties
export const addNewProperty = (property: Omit<Listing, 'id'>) => {
	const newProperty: Listing = {
		...property,
		id: `user-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`
	}
	
	// In a real app, this would be saved to a database
	// For now, we'll store in localStorage to persist across page refreshes
	if (isBrowser) {
		const existingUserProperties = JSON.parse(localStorage.getItem('userProperties') || '[]')
		const updatedUserProperties = [...existingUserProperties, newProperty]
		localStorage.setItem('userProperties', JSON.stringify(updatedUserProperties))
	}
	
	return newProperty
}

// Function to get all properties (including user-added ones)
export const getAllProperties = (): Listing[] => {
	if (!isBrowser) {
		// Return only static listings during server-side rendering
		return listings
	}
	
	const userProperties = JSON.parse(localStorage.getItem('userProperties') || '[]')
	return [...listings, ...userProperties]
}
